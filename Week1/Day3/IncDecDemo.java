public class IncDecDemo{
public static void main(String[] args){
int x=10;
System.out.println("Initial value of x = " + x);
System.out.println("x++ = " + (x++));
 System.out.println("After x++ , x = " + x);
System.out.println("++x = " + (++x));
System.out.println("x-- = " + (x--)); 
System.out.println("After x-- , x = " + x);
System.out.println("--x = " + (--x));
}
}
